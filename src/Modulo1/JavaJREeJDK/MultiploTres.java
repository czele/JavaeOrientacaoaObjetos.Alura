package Modulo1.JavaJREeJDK;

//Utilize um laço do tipo for para imprimir todos os múltiplos de 3, entre 1 e 100.
public class MultiploTres {
    public static void main(String[] args) {
        for (int multiploTres = 1; multiploTres <= 100; multiploTres++) {
            System.out.println(multiploTres * 3);
        }
    }
}
